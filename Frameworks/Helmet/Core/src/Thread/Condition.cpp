#include "Condition.hpp"

#include <Helmet/Core/Thread/CriticalSection.hpp>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Helmet {
namespace Core {
namespace Thread {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Condition::Condition(bool _initialState)
:   m_condition()
,   m_exclusiveAccessLock()
,   m_isAsserted(_initialState)
,   m_threadsAreBlocked(false)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Condition::~Condition()
{
    if (m_threadsAreBlocked) {
        assertCondition();  // wake up all waiting threads (in a
                            // properly-designed application, there should be
                            // none)
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_Condition&
Condition::assertCondition()
{
    m_exclusiveAccessLock.lock();
    m_isAsserted = true;
    if (m_threadsAreBlocked) {
        m_threadsAreBlocked = false;    // all currently-blocked threads are about to be awakened
        m_condition.notify_all();
    }

    m_exclusiveAccessLock.unlock();
    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_Condition&
Condition::retractCondition()
{
    m_exclusiveAccessLock.lock();
    m_isAsserted = false;

    m_exclusiveAccessLock.unlock();
    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_Condition&
Condition::requireCondition()
{
    m_exclusiveAccessLock.lock();
    if (!m_isAsserted) {
        m_threadsAreBlocked = true;
        m_condition.wait(m_exclusiveAccessLock);
    }

    m_exclusiveAccessLock.unlock();

    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Thread
}   // namespace Core
}   // namespace Helmet
