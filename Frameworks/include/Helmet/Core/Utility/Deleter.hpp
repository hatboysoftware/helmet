#pragma once

#include <assert.h>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Helmet {
namespace Core {
namespace Utility {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

template <typename Base, typename Derived>
class Deleter
{
    /// @name Types
    /// @{
public:
    /// @}

    /// @name Deleter<Base, Derived> implementation
    /// @{
public:
    void operator()(Base* _pObject) const
    {
        auto pObject = dynamic_cast<Derived*>(_pObject);
        assert(pObject != nullptr);
        delete pObject;
    }
    /// @}

    /// @name 'Structors
    /// @{
public:
    Deleter() {}
    ~Deleter() {}
    /// @}

};  // template class Deleter

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Utility
}   // namespace Core
}   // namespace Helmet
