const std::string&
Plugin::getName() const
{
    return m_pModule->getName();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Plugin::pBlockchainNode_type
Plugin::getNode(Helmet::Enterprise::I_ApplicationServer& _applicationServer,
                const std::string& _name) const
{
    return pBlockchainNode_type(new BlockchainNode(_applicationServer, _name),
                                Helmet::Core::Utility::Deleter<
                                        Helmet::Blockchain::I_BlockchainNode,
                                        BlockchainNode
                                >());
}

