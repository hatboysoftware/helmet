Plugin::pProtocolService_type
Plugin::getProtocolService(Helmet::Enterprise::I_ApplicationServer& _applicationServer,
                           const std::string& _name) const
{
    return pProtocolService_type(new ProtocolService(_applicationServer, _name),
                                 Helmet::Core::Utility::Deleter<
                                        Helmet::Enterprise::I_ProtocolService,
                                        ProtocolService
                                >());
}

