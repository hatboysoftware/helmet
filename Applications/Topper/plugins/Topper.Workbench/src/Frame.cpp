//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Frame::Frame(const std::string& _name,
             wxWindow& _parent)
:   wxPanel(&_parent)
{
    SetName(_name);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Frame::~Frame()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string Frame::getName() const
{
    return std::string(GetName().mb_str());
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void Frame::setName(const std::string& _name)
{
    SetName(_name);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void Frame::addChildFrame(I_Frame& _frame)
{
    auto pFrame = dynamic_cast<Frame*>(&_frame);
    if (pFrame != nullptr)
    {
        AddChild(pFrame);
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void Frame::removeChildFrame(I_Frame& _frame)
{
    auto pFrame = dynamic_cast<Frame*>(&_frame);
    if (pFrame != nullptr)
    {
        RemoveChild(pFrame);
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void Frame::getChildFrames(I_FrameVisitor& _visitor)
{
    _visitor.begin();
    auto pChildren = GetChildren();
    for (auto pChild = pChildren.GetFirst(); pChild; pChild = pChild->GetNext())
    {
        auto pWindow = pChild->GetData();
        auto pFrame = dynamic_cast<I_Frame*>(pWindow);
        assert(pFrame != nullptr);
        _visitor.visit(*pFrame);
    }

    _visitor.end();
}

